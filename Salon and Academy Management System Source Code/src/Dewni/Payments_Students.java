    private void combo_feeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_combo_feeMouseClicked
       //----------mouse click for load course id-------//
        combo_fee.removeAllItems();
        course = (String) combo_course.getSelectedItem();
        
        String query = "SELECT courseFee from course_details WHERE title = '"+course+"'";
        
        double item1;
        int count;

        try {
            ps = dbc.con.prepareStatement(query);
            rs = this.ps.executeQuery();

            while (rs.next()) {
                item1 = rs.getDouble("courseFee");
                String fee =Double.toString(item1);
                combo_fee.addItem(fee);

            }

        } catch (SQLException err) {
            System.out.println(err.getMessage());
        }
    }//GEN-LAST:event_combo_feeMouseClicked

