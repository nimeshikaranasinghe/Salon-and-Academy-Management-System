    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed

        ////////////////////student_id vali/////////////////////////
        if (txt_id.getText().equals(""))
        {
            error_message error = new error_message(null, true);
            error.setMessage("Enter a Student ID");
            error.show();
            txt_id.requestFocus();
            return;
        }

        if(txt_id.getText().charAt(0) != 'E')
        {
            error_message error = new error_message(null, true);
            error.setMessage("Studentid start with 'S''");
            error.show();
            txt_id.requestFocus();
            return;

        }

        //------------COURSE VALI-----------//
        if (combo_course.getSelectedItem() == null)
        {
            error_message error = new error_message(null, true);
            error.setMessage("Select a course");
            error.show();
            combo_course.requestFocus();
            return;
        }

        //-------------COURSE FEE VALI-----------//
        if (combo_fee.getSelectedItem() == null)
        {
            error_message error = new error_message(null, true);
            error.setMessage("Invalid fee");
            error.show();
            combo_fee.requestFocus();
            return;
        }

        //--------------CURRENT PAYMENT-----------
        if (txt_payment.getText().equals(""))
        {
            error_message error = new error_message(null, true);
            error.setMessage("Invalid Payment");
            error.show();
            txt_payment.requestFocus();
            return;
        }

        //-----------PAYMENT TYPE---------//

        radio_cash.setActionCommand("Cash");
        radio_credit.setActionCommand("Credit card");

        boolean cash = radio_cash.isSelected();
        boolean credit= radio_credit.isSelected();

        String type = null;

        if ((cash == true && credit == false ) || (credit == true && cash == false ))
        {
            type = button_payments.getSelection().getActionCommand();
        }

        else
        {
            error_message error = new error_message(null, true);
            error.setMessage("Choose a Payment type");
            error.show();
            return;
        }

        //-----------------DATE PAYMENT--------//

/*        String date =((JTextField)date_date.getDateEditor().getUiComponent()).getText();

        if(date.equals("")){

            error_message err = new error_message(null, true);
            err.setMessage("Date not valid");
            err.show();
            date_date.requestFocus();
            return;

        }*/

        String stdid = txt_id.getText();
        String course = (String) combo_course.getSelectedItem();
        double fee  = Double.parseDouble((String) combo_fee.getSelectedItem());
        double payment = Double.parseDouble(txt_payment.getText());
        String date = lbl_date.getText();
        double remain;

        try{

            remain= fee-payment;
            String sql = "insert into Payments values('"+stdid+"','"+course+"','"+fee+"','"+payment+"','"+type+"','"+date+"','"+remain+"')";
            new Payments_Home(stdid,course,fee,payment,type,date,remain).setVisible(true);
//            JOptionPane.showMessageDialog(null, "insert into !!!");
            ps = dbc.con.prepareStatement(sql);

//            JOptionPane.showMessageDialog(null, "prepare !!!");
            ps.execute();
//            JOptionPane.showMessageDialog(null, "execute !!!");
        }
        catch(Exception e){}
    }//GEN-LAST:event_btn_addActionPerformed

