    private void btn_calcActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_calcActionPerformed

        pnl_sal.setVisible(true);
        
        //--- show salary details
        if (this.postid == 101 || this.postid == 105 || this.postid == 106) {
            this.salary = sc.setDetailsForManAcRe(this.empid, this.postid, this.leaves, this.shortL, this.ot);
            
            this.salary = round(this.salary, 2); //round salary to 2 decimal places
            
            lbl_month.setText(this.yearS + "-" + this.monthS + "-" + "01   to  " + this.yearS + "-" + this.monthS + "-" + "30");
            lbl_salary.setText(Double.toString(this.salary));
            
            sc.sendSalary(empid, this.yearS + "-" + this.monthS + "-" + "30", salary);      //send salary to database          
            
        }
        
        else if (this.postid == 103) {
                this.salary = sc.calcSalForLec(this.empid, this.postid);
                
                this.salary = round(this.salary, 2); //round salary to 2 decimal places
            
                lbl_month.setText(this.yearS + "-" + this.monthS + "-" + "01   to  " + this.yearS + "-" + this.monthS + "-" + "30");
                lbl_salary.setText(Double.toString(this.salary));

                sc.sendSalary(empid, this.yearS + "-" + this.monthS + "-" + "30", salary);      //send salary to database          

            }
            
        else{
            this.salary = sc.calcSalForHora(this.empid, this.postid);
            
                this.salary = round(this.salary, 2); //round salary to 2 decimal places
            
                lbl_month.setText(this.yearS + "-" + this.monthS + "-" + "01   to  " + this.yearS + "-" + this.monthS + "-" + "30");
                lbl_salary.setText(Double.toString(this.salary));

                sc.sendSalary(empid, this.yearS + "-" + this.monthS + "-" + "30", salary);      //send salary to database          

        }
            
        
        
    }//GEN-LAST:event_btn_calcActionPerformed

