    public void setIDandPost(String pName){
        
        String query = "";
        String fullname = pName;
        String name[] = {"", "", ""};
        String post = "";
        
        //---seperate employee name
        StringTokenizer token = new StringTokenizer(fullname, " ");
        int round = 0;
        while (token.hasMoreTokens()) {
            if (round==2) 
                name[round-1] = name[round-1] + " " + token.nextToken(); 
            else
                name[round] = token.nextToken(); 
            
            round++;
            
        }
        System.out.println(name[0] + " " + name[1]);
        
        try {
            //--find employee id
            query = "SELECT * FROM Employee WHERE empFName = '"+ name[0] +"' AND empLName = '"+ name[1]+ "'";
            pst = dbc.con.prepareStatement(query);
            rs = this.pst.executeQuery();

            if (rs.next()) {
                this.empid = rs.getInt(1);
            }
            
            //find employee post
            post = sc.getPostName(this.empid);
            
            //---set Feilds
            lbl_id.setText(Integer.toString(empid));
            lbl_post.setText(post);
            
            //--find post id
            this.postid = sc.getPostID(this.empid);
            System.out.println(this.postid);
            
            if (this.postid == 101 || this.postid == 105 || this.postid == 106) {
                setFeildsEnableForManAcRe();
                setDetailsForManAcRe(this.empid, this.postid);
   
            }
            
            
            
        } 
        catch (SQLException err) {
            JOptionPane.showMessageDialog(this, err);
        }
        
    }
    
    //======set details to relevant feilds for manager, receptionist, accoutant=================================//
    public void setDetailsForManAcRe(int pEmpid, int pPostid){
        this.empid = pEmpid;
        this.postid = pPostid;
        
        int annLeaves = 0;
        int annSLeaves = 0;
        int presentDates = 0;
        
        String query = "";
        
        
        try {
            //get present dates by employee
            query = "SELECT COUNT(empId) as presentDates FROM EmpAttendance WHERE empId = "+ this.empid +" AND today BETWEEN '"+ this.yearS +"-"+ this.monthS+"-01' AND '"+ this.yearS +"-"+ this.monthS +"-31'";
            pst = dbc.con.prepareStatement(query);
            rs = this.pst.executeQuery();

            if (rs.next()) {
                presentDates = rs.getInt(1);
            }
            
            //get post details according to employee's post
            query = "SELECT * FROM PostSalDetails WHERE postId = "+ this.postid +"";
            pst = dbc.con.prepareStatement(query);
            rs1 = this.pst.executeQuery();

            if (rs1.next()) {
                annLeaves = rs1.getInt(4);
                annSLeaves = rs1.getInt(5);
            }

            //get leave details of employee
            query = "SELECT * FROM EmpLeaveDetails WHERE empId = "+ this.empid+"";
            pst = dbc.con.prepareStatement(query);
            rs1 = this.pst.executeQuery();

            if (rs1.next()) {
                this.shortL = rs1.getInt(3);
                this.ot = rs1.getInt(4);
            }
            
            //get additional leavings taken by the employee
            this.leaves = 31 - 8 - presentDates - annLeaves;
            if (this.leaves < 0) {
                this.leaves = 0;
            }
            
            //get additional short leaves
            this.shortL = this.shortL - annSLeaves;
            if (this.shortL < 0) {
                this.shortL = 0;
            }
            
            //---set labes with relevant details
            lbl_leaves.setText(Integer.toString(this.leaves));
            lbl_sLea.setText(Integer.toString(this.shortL));
            lbl_OThrs.setText(Integer.toString(this.ot));
            
        } 
        catch (SQLException err) {
            JOptionPane.showMessageDialog(this, err);
        }
        
        
        
    }
        
    //round value to decimal places
    public static double round(double value, int places) {
        if (places < 0) throw new IllegalArgumentException();

        long factor = (long) Math.pow(10, places);
        value = value * factor;
        long tmp = Math.round(value);
        return (double) tmp / factor;
    }

    
     
    
  
